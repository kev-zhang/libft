int	ft_is_prime(int nb)
{
	int a;
	int sqrt;
	int x;

	a = 2;
	sqrt = nb;
	x = 1;
	if (nb <= 1)
		return (0);
	while (sqrt - x > 0)
	{
		sqrt = (sqrt + x) / 2;
		x = nb / sqrt;
	}
	while (a < sqrt + 1)
	{
		if (nb % a == 0)
			return (0);
		a++;
	}
	return (1);
}
