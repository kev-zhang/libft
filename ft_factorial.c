int	ft_factorial(int nb)
{
	if (nb < 0 || nb >= 13)
		return (0);
	if (nb == 0)
		return (1);
	if (nb >= 1)
	{
		nb = nb * ft_factorial(nb - 1);
	}
	return (nb);
}
