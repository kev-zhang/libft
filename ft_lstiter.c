#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*temp;

	if (f)
	{
		while (lst)
		{
			temp = lst;
			lst = lst->next;
			f(temp);
		}
	}
}
