#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t c;

	c = 0;
	while (c < n)
	{
		((char*)dest)[c] = ((char*)src)[c];
		c++;
	}
	return (dest);
}
